    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
       
         empty.setText("");
        try{
          DefaultTableModel model=(DefaultTableModel)spisak.getModel();
          model.setRowCount(0); 
          
   
            Statement st=con.createStatement();
            
            ResultSet rs=st.executeQuery
        ("Select ime,prezime,datum_i_vrijeme_zaduzenja,ID_knjige,Odjeljenje from zaduzeno where Naziv_djela='"+naziv_knjige.getText()+"'");
           String data[]=new String[4];
            while(rs.next()){
                data[0]=rs.getString("Ime")+" "+rs.getString("Prezime");
               
                data[1]=rs.getString("Odjeljenje");
              data[2]=rs.getString("ID_knjige");
              data[3]=rs.getString("Datum_i_vrijeme_zaduzenja");
              model.addRow(data);
            }
           
           int count=model.getRowCount();
           
           if(count==0){
               empty.setText("Nijedna knjiga sa ovim nazivom  nije zadu탑ena!");
           }
       }
       
       
       catch (SQLException ex) {
            Logger.getLogger(JfindBook.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

